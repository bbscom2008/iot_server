// 构建树形结构数据
export function buildTreeData(provinceCityData) {
    return provinceCityData.map((item) => ({
        id: item.province,
        label: item.province,
        children: item.cities.map((city) => ({
            id: `${item.province}-${city}`,
            label: city,
            parent: item.province,
            isCity: true
        })),
        isProvince: true
    }))
}

// 根据选中的树数据转换为所需格式
export function convertToRegionConfig(checkedNodes) {
    const regionMap = new Map()

    checkedNodes.forEach((node) => {
        if (node.isCity) {
            const province = node.parent
            if (!regionMap.has(province)) {
                regionMap.set(province, {
                    province: province,
                    cities: []
                })
            }
            regionMap.get(province).cities.push(node.label)
        } else if (node.isProvince) {
            // 如果省份被选中，表示所有城市
            if (!regionMap.has(node.label)) {
                regionMap.set(node.label, {
                    province: node.label,
                    cities: []
                })
            }
        }
    })

    return Array.from(regionMap.values())
}

// 根据 regionConfig 数据恢复树节点的选中状态
export function restoreTreeCheckedNodes(regionConfig, treeData) {
    const checkedNodes = []
    const checkedKeys = []

    if (!regionConfig || regionConfig.length === 0) {
        return { checkedNodes, checkedKeys }
    }

    regionConfig.forEach((region) => {
        const province = region.province
        const cities = region.cities || []

        if (cities.length === 0) {
            // 如果 cities 为空，表示选中整个省份
            checkedKeys.push(province)
            const provinceNode = treeData.find((node) => node.label === province)
            if (provinceNode) {
                checkedNodes.push(provinceNode)
            }
        } else {
            // 选中指定的城市
            cities.forEach((city) => {
                const nodeId = `${province}-${city}`
                checkedKeys.push(nodeId)
                const provinceNode = treeData.find((node) => node.label === province)
                if (provinceNode) {
                    const cityNode = provinceNode.children.find((child) => child.label === city)
                    if (cityNode) {
                        checkedNodes.push(cityNode)
                    }
                }
            })
        }
    })

    return { checkedNodes, checkedKeys }
}


